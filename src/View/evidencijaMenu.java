    public void prikaziTuriste(){
        //funkcija za prikaz turista u tablicu 
        ArrayList<evidencijaModel> list =  Controller.turistList();
        DefaultTableModel model = (DefaultTableModel)turistTablica.getModel();
        Object[] row = new Object[6];
        for (int i = 0;i < list.size();i++){
            row[0] = list.get(i).getTuristId();
            row[1] = list.get(i).getTuristIme();
            row[2] = list.get(i).getTuristPrezime();
            row[3] = list.get(i).getOibTurist();
            row[4] = list.get(i).getMailTurist();
            row[5] = list.get(i).getMobTurist();
            model.addRow(row);
    }
    }

