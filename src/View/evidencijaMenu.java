    private void evidentirajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_evidentirajButtonActionPerformed
        //ako su polja prazna ispisi upozorenje 
        try {
        if (imeField.getText().isEmpty() || prezimeField.getText().isEmpty() || oibField.getText().isEmpty() || mailField.getText().isEmpty() || mobField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ispunite sva polja","Upozorenje!", JOptionPane.WARNING_MESSAGE);
        }else {
            // unos u bazu
        String imeTurista = imeField.getText();
        String prezimeTurista = prezimeField.getText();
        String oibTurista = oibField.getText();
        String mailTurista = mailField.getText();
        String mobTurista = mobField.getText();
        //poziv evidentiranjeTurista za spremanje podataka u bazu
        Controller.evidentiranjeTurista(imeTurista, prezimeTurista, oibTurista, mailTurista, mobTurista);
        // ispis uspjesnog spremanja
        JOptionPane.showMessageDialog(null, "Spremanje u bazu uspjelo!","Informacija!", JOptionPane.INFORMATION_MESSAGE);
        //zvanje prikazi turiste kako bi napravili refresh na tablici odma nakon evidentiranja setiranje row na 0
        DefaultTableModel model = (DefaultTableModel)turistTablica.getModel();
        model.setRowCount(0);
        prikaziTuriste();
        }
        }
        catch(SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_evidentirajButtonActionPerformed

